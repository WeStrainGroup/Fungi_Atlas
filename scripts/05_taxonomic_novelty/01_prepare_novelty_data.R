# --- Process the Cell 2015 dataset ---
# Count ASV reads after filtering relative abundance > 1/10,000
input_dir <- "/data/wangxinyu/ITS_Public/Project/Analysis/blast/cell2025/raw/asv_tables/"
file_list <- list.files(input_dir, pattern = "\\.txt$", full.names = TRUE)
file_list

threshold_depth <- 10000
threshold_abund <- 1 / 10000
total_counts_list <- list()
total_files <- length(file_list)

for (i in seq_along(file_list)) {
  file <- file_list[i]
  cat(sprintf("Processing file %d of %d: %s\n", i, total_files, basename(file)))

  dt <- fread(file)

  # Assume the first column is ASV ID and rename to 'ASV'
  asv_col <- names(dt)[1]
  setnames(dt, asv_col, "ASV")
  setkey(dt, ASV)

  sample_cols <- setdiff(names(dt), "ASV")

  # Remove sample columns with total reads < 10,000
  sample_totals <- colSums(dt[, ..sample_cols])
  sample_cols_to_keep <- names(sample_totals[sample_totals >= threshold_depth])
  dt <- dt[, c("ASV", sample_cols_to_keep), with = FALSE]

  if (length(sample_cols_to_keep) == 0) {
    cat("  Skipping: all samples below read threshold.\n")
    next
  }

  # For each sample, compute relative abundance and set values below threshold to 0
  for (col in sample_cols_to_keep) {
    total <- sum(dt[[col]], na.rm = TRUE)
    if (total > 0) {
      rel_abund <- dt[[col]] / total
      dt[[col]][rel_abund < threshold_abund] <- 0
    }
  }

  # Sum counts per ASV
  dt[, total_abundance := rowSums(.SD), .SDcols = sample_cols_to_keep]

  # Keep ASV and total_abundance
  total_counts_list[[i]] <- dt[, .(ASV, total_abundance)]

  # Cleanup
  rm(dt)
  gc()
}


merged_counts <- rbindlist(total_counts_list, use.names = TRUE)
final_summary <- merged_counts[, .(counts = sum(total_abundance)), by = ASV]
setorder(final_summary, -counts)
